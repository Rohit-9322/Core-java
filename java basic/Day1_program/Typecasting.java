package Day1_program;

public class Typecasting {
    public static void main(String[] args) {

        // 1 Double to int 

        double D = 10.21;
        int A = (int)D;
        System.out.println(A);

        // 2 int to Double 
        int C = 45;
        double B = C;
        System.out.println(B);

        // 3 
        int a = 150;
        long b = a;
        float c = b;

        System.out.println(c);





    }
}

package Day1_program;

public class ASC2 {
    public static void main(String[] args) {
        
        // Printing A to z alphabite
        char ch = 'A';
        int acsii = ch;
        System.out.println("ASC2 Is " + ch + " Is " + acsii); // 65

        // a to z 
        for(char c = 'A'; c <= 'Z'; c++){
            System.out.print(c+" ");
        }
    }
}
